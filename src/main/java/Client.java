public class Client {
    private String name;
    private int age;
    private Sex sex;
    private String mail;

    Client(String name, int age, Sex sex, String mail){
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.mail = mail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Sex getSex() {
        return sex;
    }

    public void setSex(Sex sex) {
        this.sex = sex;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }
}